include!(concat!(env!("OUT_DIR"), "/logos.rs")); // include the logos.rs file generated by build.rs

use clap::Parser;
use crate::info::SystemInfo;
mod info;
mod desktop;

mod colour;

mod config;

mod cli;

fn main() {
    //colored::control::set_override(true);

    let args = cli::Args::parse(); // parse the command line arguments
    if args.list_logos { // if the list-logos flag is set, list all available logos
        for name in list_logos() {
            println!("{}", name);
        }
        return;
    }


    let mut config = config::Config::load();

    let info = SystemInfo::new();

    if let Some(path) = args.wallpaper_path { // if the wallpaper path is specified in the command line arguments, set it in the config
        if std::path::Path::new(&path).exists() { // if the wallpaper exists, set it in the config
            config.wallpaper_path = Some(path);
            config.save();
        }
    }


    let wallpaper = config.wallpaper_path.clone().or_else(|| colour::detect_wallpaper(&info.desktop_environment.name)); // detect the wallpaper if it's not specified in the config

    let brightness = config.brightness.unwrap_or(1.0).clamp(0.0, 2.0); // get the brightness from the config or use the default value
                                                                                            // clamp the brightness to be between 0.0 and 2.0 

    let palette = match wallpaper { // extract the palette from the wallpaper and correct the brightness
        Some(ref path) => colour::correct_brightness(&colour::extract_palette(path), brightness),
        None => vec![
            color_thief::Color { r: 100, g: 150, b: 255 }, // blue
            color_thief::Color { r: 200, g: 100, b: 255 }, // purple
            color_thief::Color { r: 150, g: 200, b: 255 }, // light blue
            color_thief::Color { r: 180, g: 180, b: 180 }, // grey
        ]  //  if no wallpaper is detected, use the default palette
    };




    let id = SystemInfo::read_os_id();

    let logo_name = if let Some(logo) = args.preview_logo {
        if list_logos().contains(&logo.as_str()) {
            logo  // just for this run, don't save
        }
        else {
            config.logo.as_deref().unwrap_or(&id).to_string() // if the logo is not in the list of available logos, use the default or last saved logo
        }

    } else if let Some(logo) = &args.set_logo { // if the logo is specified in the command line arguments, set it in the config
        if list_logos().contains(&logo.as_str()) {
            config.logo = Some(logo.to_string());  // save to config
            config.save();  // write to config.toml
            config.logo.as_deref().unwrap_or(&id).to_string() // read from config
        }
        else {
            config.logo.as_deref().unwrap_or(&id).to_string() // if the logo is not in the list of available logos, use the default or last saved logo
        }
    } else {
        config.logo.as_deref().unwrap_or(&id).to_string() // if the logo is not specified in the command line arguments, use the default or last saved logo
    };

    let info_lines = info.to_lines(&palette, &config);

    let logo = get_logo(&logo_name).unwrap_or("unknown");
    let logo_lines = logo.lines().collect::<Vec<&str>>();

    // temporary closure to strip the placeholder from the logo
    let strip_placeholders = |s: &str| -> String {
        let mut result = s.to_string();
        for i in 1..=9 {
            result = result.replace(&format!("${}", i ), "")
        }
        result
    };

    let logo_width = logo_lines.iter().map(|line| strip_placeholders(line).chars().count()).max().unwrap_or(0); // find the width of the logo by finding the longest line

    let max_lines = logo_lines.len().max(info_lines.len()); // find the greater of the two lengths for the width of the logo output
    let empty = String::new();




    for i in 0..max_lines {
        let logo_line = logo_lines.get(i).unwrap_or(&"");
        let info_line = info_lines.get(i).unwrap_or(&empty);

        let stripped = strip_placeholders(logo_line);
        let substituted = colour::substitute_placeholders(logo_line, &palette);
        let padding = " ".repeat(logo_width - stripped.chars().count());

        println!("{}{}  {}", substituted, padding, info_line);
    }

}


